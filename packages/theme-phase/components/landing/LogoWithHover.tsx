export type LogoName = keyof typeof logos;

interface LogoWithHoverProps {
  name: LogoName;
  className?: string;
  height?: number;
  width?: string;
}

export function LogoWithHover({ name, className = '', height, width = 'auto' }: LogoWithHoverProps) {
  const logo = logos[name];
  const [isHovered, setIsHovered] = React.useState(false);

  // Special handling for logos with gradient or color swaps
  const isDoubleZero = name === 'doublezero';
  const isSolana = name === 'solana';
  const hasSimpleHover = !isDoubleZero && !isSolana;

  // Solana and DoubleZero use SVG filter for gradient/color glow, others use CSS drop-shadow
  const glowFilter = isHovered && !isSolana && !isDoubleZero
    ? `drop-shadow(0 0 12px ${logo.glowColors[0]}) drop-shadow(0 0 30px ${logo.glowColors[1]}) drop-shadow(0 0 60px ${logo.glowColors[2]})`
    : 'drop-shadow(0 0 0px transparent)';

  return (
    <svg
      width={logo.width}
      height={logo.height}
      viewBox={logo.viewBox}
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      overflow="visible"
      className={`transition-all duration-300 hover:scale-110 cursor-pointer ${className}`}
      style={{ width, height: height ? `${height}px` : 'auto', filter: glowFilter, overflow: 'visible' }}
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
    >
      {/* Solana with gradient */}
      {isSolana && (() => {
        const solanaLogo = logos.solana;
        return (
          <>
            <defs>
              <linearGradient
                id="solanaGradient"
                x1={solanaLogo.gradient.x1}
                y1={solanaLogo.gradient.y1}
                x2={solanaLogo.gradient.x2}
                y2={solanaLogo.gradient.y2}
                gradientUnits="userSpaceOnUse"
              >
                {solanaLogo.gradient.stops.map((stop, i) => (
                  <stop key={i} offset={stop.offset} stopColor={stop.color} />
                ))}
              </linearGradient>
              <filter id="solanaGlow" x="-200%" y="-200%" width="500%" height="500%">
                <feGaussianBlur in="SourceGraphic" stdDeviation="50" result="blur" />
                <feColorMatrix in="blur" type="saturate" values="2" result="saturated" />
                <feMerge>
                  <feMergeNode in="saturated" />
                  <feMergeNode in="saturated" />
                </feMerge>
              </filter>
            </defs>
            {/* Gradient glow layer (blurred gradient behind) */}
            {solanaLogo.paths.map((d, i) => (
              <path
                key={`glow-${i}`}
                d={d}
                fill="url(#solanaGradient)"
                filter="url(#solanaGlow)"
                className="transition-opacity duration-300"
                style={{ opacity: isHovered ? 0.8 : 0 }}
              />
            ))}
            {/* BW version */}
            {solanaLogo.paths.map((d, i) => (
              <path
                key={`bw-${i}`}
                d={d}
                fill="var(--glass-bg)"
                stroke="var(--glass-border)"
                className="transition-opacity duration-300"
                style={{ opacity: isHovered ? 0 : 1 }}
              />
            ))}
            {/* Hover version - cream color */}
            {solanaLogo.paths.map((d, i) => (
              <path
                key={`hover-${i}`}
                d={d}
                fill="#f3eed9"
                className="transition-opacity duration-300"
                style={{ opacity: isHovered ? 1 : 0 }}
              />
            ))}
          </>
        );
      })()}

      {/* DoubleZero with color swap */}
      {isDoubleZero && (() => {
        const dzLogo = logos.doublezero;
        return (
          <>
            <defs>
              <filter id="doubleZeroGlow" x="-300%" y="-300%" width="700%" height="700%">
                <feGaussianBlur in="SourceGraphic" stdDeviation="80" result="blur" />
                <feColorMatrix in="blur" type="saturate" values="3" result="saturated" />
                <feMerge>
                  <feMergeNode in="saturated" />
                  <feMergeNode in="saturated" />
                  <feMergeNode in="saturated" />
                </feMerge>
              </filter>
            </defs>
            {/* Simplified color glow layer - basic shapes */}
            <g style={{ transform: 'scale(1.3)', transformOrigin: 'center', opacity: isHovered ? 1 : 0 }} className="transition-opacity duration-300">
              {/* Green square - top left */}
              <rect x="65" y="50" width="65" height="65" fill="#29F91E" filter="url(#doubleZeroGlow)" />
              {/* Red square - middle right */}
              <rect x="145" y="130" width="65" height="65" fill="#E80000" filter="url(#doubleZeroGlow)" />
              {/* Blue triangle - bottom left */}
              <polygon points="50,150 130,230 50,230" fill="#16B5F4" filter="url(#doubleZeroGlow)" />
            </g>
            {/* BW version */}
            {dzLogo.bwPaths.map((d, i) => (
              <path
                key={`bw-${i}`}
                d={d}
                fill="var(--glass-bg)"
                stroke="var(--glass-border)"
                className="transition-opacity duration-300"
                style={{ opacity: isHovered ? 0 : 1 }}
              />
            ))}
            {/* Color version */}
            {dzLogo.colorPaths.map((path, i) => (
              <path
                key={`color-${i}`}
                d={path.d}
                fill={path.fill}
                className="transition-opacity duration-300"
                style={{ opacity: isHovered ? 1 : 0 }}
              />
            ))}
          </>
        );
      })()}

      {/* Simple logos with fill/stroke change */}
      {hasSimpleHover && 'paths' in logo &&
        (logo.paths as readonly string[]).map((d, i) => (
          <path
            key={i}
            d={d}
            fill={isHovered ? ((logo as { hoverFill?: string }).hoverFill || 'var(--glass-bg)') : 'var(--glass-bg)'}
            stroke={isHovered ? ((logo as { hoverStroke?: string }).hoverStroke || 'var(--glass-border)') : 'var(--glass-border)'}
            className="transition-all duration-300"
          />
        ))}
    </svg>
  );
}

export default LogoWithHover;
